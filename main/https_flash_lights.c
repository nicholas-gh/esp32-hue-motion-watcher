static const char *TAG = "main";

#define HUE_FLASH_WEB_SERVER "172.20.10.131"
#define HUE_FLASH_WEB_PORT 80
#define HUE_FLASH_WEB_URL "/api/Opu5owbSVbMIfPraSfn6uI1ZMgXjaSu-yNOJMRW7/groups/1/action"

static const char *FLASH_REQUEST = "PUT " HUE_FLASH_WEB_URL " HTTP/1.0\r\n"
    "Host: "HUE_FLASH_WEB_SERVER"\r\n"
    "Content-Type: application/x-www-form-urlencoded\r\n"
    "Content-Length: 18\r\n"
    "\r\n"
    "{\"alert\":\"select\"}";

void hue_flashlights_task(void *pvParameters)
{
    const struct addrinfo hints = {
        .ai_family = AF_INET,
        .ai_socktype = SOCK_STREAM,
    };
    struct addrinfo *res;
    struct in_addr *addr;
    int s, r;
    char recv_buf[512];
    
        /* Wait for the callback to set the CONNECTED_BIT in the
           event group.
        */
        xEventGroupWaitBits(wifi_event_group, CONNECTED_BIT,
                            false, true, portMAX_DELAY);
        ESP_LOGI(TAG, "Connected to AP");

        int err = getaddrinfo(HUE_FLASH_WEB_SERVER, "80", &hints, &res);

        if(err != 0 || res == NULL) {
            ESP_LOGE(TAG, "DNS lookup failed err=%d res=%p", err, res);
            vTaskDelay(1000 / portTICK_PERIOD_MS);
            return;
        }

        /* Code to print the resolved IP.

           Note: inet_ntoa is non-reentrant, look at ipaddr_ntoa_r for "real" code */
        addr = &((struct sockaddr_in *)res->ai_addr)->sin_addr;
        ESP_LOGI(TAG, "DNS lookup succeeded. IP=%s", inet_ntoa(*addr));

        s = socket(res->ai_family, res->ai_socktype, 0);
        if(s < 0) {
            ESP_LOGE(TAG, "... Failed to allocate socket.");
            freeaddrinfo(res);
            vTaskDelay(1000 / portTICK_PERIOD_MS);
            return;
        }
        ESP_LOGI(TAG, "... allocated socket\r\n");

        if(connect(s, res->ai_addr, res->ai_addrlen) != 0) {
            ESP_LOGE(TAG, "... socket connect failed errno=%d", errno);
            close(s);
            freeaddrinfo(res);
            vTaskDelay(4000 / portTICK_PERIOD_MS);
            return;
        }

        ESP_LOGI(TAG, "... connected");
        freeaddrinfo(res);

        if (write(s, FLASH_REQUEST, strlen(FLASH_REQUEST)) < 0) {
            ESP_LOGE(TAG, "... socket send failed");
            close(s);
            vTaskDelay(4000 / portTICK_PERIOD_MS);
            return;
        }
        ESP_LOGI(TAG, "... socket send success");

        /* Read HTTP response */
	int offset = 0;
	bzero(recv_buf, sizeof(recv_buf));
        do {
            r = read(s, recv_buf+offset, sizeof(recv_buf)-offset-1);
	    offset += r;
        } while(r > 0);

	ESP_LOGI(TAG, "Got: %s", recv_buf);
	
        close(s);

	ESP_LOGI(TAG, "Closed socket");	
}

